// Створити масив, довжину та елементи якого задає користувач.
// Відсортувати масив за зростанням.
// Видалити елементи з масиву з 2 по 4 (включно!).
// У міру змін виводити вміст масиву на сторінку.

let arr = [1,-4,10,6,-2,2,0];
console.log(`Користувачем задано масив: ${arr}`);

arr.sort( (a, b) => a - b );
console.log(`Сортування масиву за зростанням: ${arr}`);

arr.splice(1, 3);
console.log(`Видалено елементи з масиву з 2 по 4 (включно!): ${arr}`);

